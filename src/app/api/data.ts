export type Rate = number | [number, number];
export type Conversion = {
  [id: number]: [Rate, Rate, Rate];
};

const rt = (s: number): [Rate, Rate, Rate] => [
  s,
  Math.floor(s / 2),
  s >= 4 ? Math.floor(s / 4) : [2, Math.floor(s / 2)],
];

